function t(key) {
    return (i18n[currentLang] && i18n[currentLang][key]) || i18n.en[key] || key;
}

function setText(selector, key) {
    const el = document.querySelector(selector);
    if (el) el.textContent = t(key);
}

function setTextAll(selector, key) {
    document.querySelectorAll(selector).forEach(el => { el.textContent = t(key); });
}

function setPlaceholder(selector, enValue, ruValue) {
    const el = document.querySelector(selector);
    if (el) el.setAttribute('placeholder', currentLang === 'ru' ? ruValue : enValue);
}

function setOptions(selector) {
    document.querySelectorAll(selector).forEach(select => {
        const options = select.querySelectorAll('option');
        if (options[0]) options[0].textContent = t('everyone');
        if (options[1]) options[1].textContent = t('contactsOnly');
        if (options[2]) options[2].textContent = t('nobody');
    });
}

